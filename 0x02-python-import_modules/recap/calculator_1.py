#!/usr/bin/python3

"simple math calculator functions"


def add(a, b):
    """
    add:
        function to add 2 integers and return result
    """
    return (a + b)

def sub(a, b):
    """
    sub:
        function to subtract two integers
    """
    return (a - b)

def mul(a, b):
    """
    mul:
        function to multiply two integers
    """
    return (a * b)

def div(a, b):
    """
    div:
        function to divide two integers
    """
    return (a / b)


